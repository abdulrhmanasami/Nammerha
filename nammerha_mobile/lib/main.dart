/// Controls the app navigation flow:
/// Splash → Onboarding → Auth Check → Login/Dashboard
class _AppFlowController extends StatefulWidget {
  const _AppFlowController();

  @override
  State<_AppFlowController> createState() => _AppFlowControllerState();
}

class _AppFlowControllerState extends State<_AppFlowController> {
  _AppScreen _currentScreen = _AppScreen.splash;

  void _navigateTo(_AppScreen screen) {
    setState(() => _currentScreen = screen);
  }

  /// Wire push notification service to BLoC after authentication.
  void _registerPushNotifications() {
    final pushService = PushNotificationService.instance;

    // Request permission (non-blocking)
    pushService.requestPermission();

    // Register FCM token with backend
    pushService.registerToken();

    // Wire foreground messages to NotificationsBloc
    pushService.onForegroundMessage = (message) {
      if (!mounted) return;
      final notification = message.notification;
      if (notification == null) return;

      // MED-MOB-003: Create typed NotificationModel from FCM payload
      final notifData = NotificationModel.fromJson(<String, dynamic>{
        'notification_id': message.messageId ?? DateTime.now().millisecondsSinceEpoch.toString(),
        'title': notification.title ?? '',
        'body': notification.body ?? '',
        'type': message.data['type'] ?? 'general',
        'is_read': false,
        'created_at': DateTime.now().toIso8601String(),
        'data': message.data,
      });

      context.read<NotificationsBloc>().add(PushNotificationReceived(notifData));
    };

    // Wire notification tap for deep link navigation
    // P0-002 REMEDIATION: Replaced dead `// Future:` comment with live
    // deep link routing. Maps notification types → screen navigation.
    pushService.onNotificationTapped = (data) {
      debugPrint('[Nammerha Push] Deep link: $data');
      _handleDeepLink(data);
    };
  }

  /// Unregister push token on logout.
  void _unregisterPushNotifications() {
    PushNotificationService.instance.unregisterToken();
    PushNotificationService.instance.onForegroundMessage = null;
    PushNotificationService.instance.onNotificationTapped = null;
  }

  /// ═══════════════════════════════════════════════════════════════════════
  /// P0-002 REMEDIATION: Push Notification Deep Link Navigation
  /// ═══════════════════════════════════════════════════════════════════════
  /// Maps notification `type` field to the corresponding screen.
  /// Uses the global [navigatorKey] to navigate without BuildContext.
  ///
  /// Supported types:
  ///   escrow_released   → EscrowSummaryScreen
  ///   proof_verified    → WalletScreen (delivery proofs)
  ///   bid_accepted      → ProjectDetailsScreen
  ///   kyc_verified      → AdminKycScreen (admin) or DashboardScreen
  ///   kyc_rejected      → AdminKycScreen (admin) or DashboardScreen
  ///   order_status      → WalletScreen
  ///   project_funded    → ProjectDetailsScreen
  ///   project_update    → ProjectDetailsScreen
  ///   impact_update     → ImpactInboxScreen
  /// ═══════════════════════════════════════════════════════════════════════
  void _handleDeepLink(Map<String, dynamic> data) {
    final ctx = navigatorKey.currentContext;
    if (ctx == null) return;

    // P1-UXA-006 FIX: Auth guard for deep link navigation.
    // Previous: Navigated directly without checking auth state.
    // A push notification tap after session expiry would crash or show empty screen.
    // Now: Verify user is authenticated before navigating to any protected screen.
    final authState = ctx.read<AuthBloc>().state;
    if (authState is! AuthAuthenticated) {
      debugPrint('[Nammerha Push] Deep link blocked — user not authenticated.');
      return;
    }

    final type = (data['type'] ?? '').toString();
    final projectId = (data['project_id'] ?? data['projectId'] ?? '').toString();

    Widget? targetScreen;

    switch (type) {
      case 'escrow_released':
      case 'escrow_locked':
        targetScreen = const EscrowSummaryScreen();
        break;

      case 'proof_verified':
      case 'proof_submitted':
        // SUSPENDED: DonorProofScreen suspended. Redirect to Wallet.
        targetScreen = const WalletScreen();
        break;

      case 'bid_accepted':
      case 'bid_received':
      case 'project_funded':
      case 'project_update':
      case 'project_published':
        if (projectId.isNotEmpty) {
          targetScreen = ProjectDetailsScreen(projectId: projectId);
        }
        break;

      case 'kyc_verified':
      case 'kyc_rejected':
        targetScreen = const AdminKycScreen();
        break;

      case 'order_status':
      case 'payment_completed':
        targetScreen = const WalletScreen();
        break;

      case 'donation_received':
      case 'donation_refunded':
        // Donation system removed. Redirect to Wallet.
        targetScreen = const WalletScreen();
        break;

      case 'impact_update':
      case 'milestone_reached':
      case 'project_completed':
        targetScreen = const ImpactInboxScreen();
        break;

      case 'admin_escrow_pending':
        targetScreen = const AdminEscrowScreen();
        break;

      default:
        // Unknown type — no navigation, user stays on dashboard
        debugPrint('[Nammerha Push] Unknown deep link type: $type');
        return;
    }

    if (targetScreen != null) {
      Navigator.of(ctx).push(
        MaterialPageRoute(builder: (_) => targetScreen!),
      );
    }
  }

  @override
  Widget build(BuildContext context) {
    return BlocListener<AuthBloc, AuthState>(
      listener: (context, state) {
        if (state is AuthAuthenticated) {
          _registerPushNotifications();
          _navigateTo(_AppScreen.dashboard);
        } else if (state is AuthUnauthenticated) {
          if (_currentScreen == _AppScreen.dashboard) {
            _unregisterPushNotifications();
            _navigateTo(_AppScreen.login);
          }
        }
      },
      child: AnimatedSwitcher(
        duration: const Duration(milliseconds: 400),
        transitionBuilder: (child, animation) {
          return FadeTransition(
            opacity: animation,
            child: child,
          );
        },
        child: _buildScreen(),
      ),
    );
  }

  Widget _buildScreen() {
    switch (_currentScreen) {
      case _AppScreen.splash:
        return SplashScreen(
          key: const ValueKey('splash'),
          onComplete: () async {
            // Check if user has existing session
            context.read<AuthBloc>().add(AuthCheckSession());
            // P0-006 FIX: Onboarding persistence — skip if already seen.
            final prefs = await SharedPreferences.getInstance();
            final hasSeenOnboarding = prefs.getBool('hasSeenOnboarding') ?? false;
            if (hasSeenOnboarding) {
              _navigateTo(_AppScreen.login);
            } else {
              _navigateTo(_AppScreen.onboarding);
            }

            // P0-VE-001: Process any cold-start deep link after splash.
            // Must happen after navigator is mounted and flow is established.
            WidgetsBinding.instance.addPostFrameCallback((_) {
              DeepLinkService.instance.processPendingDeepLink();
            });
          },
        );
      case _AppScreen.onboarding:
        return BlocBuilder<AuthBloc, AuthState>(
          builder: (context, state) {
            // If already authenticated (from session check), skip to dashboard
            if (state is AuthAuthenticated) {
              // Using post-frame callback to avoid build-during-build
              WidgetsBinding.instance.addPostFrameCallback((_) {
                _navigateTo(_AppScreen.dashboard);
              });
            }
            return OnboardingScreen(
              key: const ValueKey('onboarding'),
              onComplete: () async {
                // P0-006 FIX: Mark onboarding as seen.
                final prefs = await SharedPreferences.getInstance();
                await prefs.setBool('hasSeenOnboarding', true);
                _navigateTo(_AppScreen.login);
              },
            );
          },
        );
      case _AppScreen.login:
        return LoginScreen(
          key: const ValueKey('login'),
          onLoginSuccess: () => _navigateTo(_AppScreen.dashboard),
        );
      case _AppScreen.dashboard:
        return BlocBuilder<AuthBloc, AuthState>(
          builder: (context, state) {
            if (state is AuthAuthenticated) {
              return DashboardScreen(
                // UNIFIED CITIZEN: Include role in key for widget tree rebuild if role changes.
                key: ValueKey('dashboard_${state.user.role}'),
                user: state.user,
                onLogout: () {
                  context.read<AuthBloc>().add(AuthLogoutRequested());
                },
              );
            }
            return const SizedBox.shrink();
          },
        );
    }
  }
}

enum _AppScreen {
  splash,
  onboarding,
  login,
  dashboard,
}

/// UXA-008 FIX: Keyboard Focus Guard
/// Unfocuses the keyboard globally whenever a route (like a BottomSheet) is popped.
/// Prevents UI fracture/jank when the keyboard and sheet animate out simultaneously.
class KeyboardDismissObserver extends NavigatorObserver {
  @override
  void didPop(Route<dynamic> route, Route<dynamic>? previousRoute) {
    FocusManager.instance.primaryFocus?.unfocus();
    super.didPop(route, previousRoute);
  }

  @override
  void didStartUserGesture(Route<dynamic> route, Route<dynamic>? previousRoute) {
    FocusManager.instance.primaryFocus?.unfocus();
    super.didStartUserGesture(route, previousRoute);
  }
}

