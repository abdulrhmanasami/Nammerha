                    GlobalCupertinoLocalizations.delegate,
                  ],
                  supportedLocales: const [
                    Locale('ar', 'SY'),
                    Locale('ar'),
                    Locale('en'),
                  ],
                  locale: activeLocale,

                  // Theme — controlled by ThemeCubit
                  themeMode: themeMode,
                  theme: NammerhaTheme.light(),
                  darkTheme: NammerhaTheme.dark(),

                  // Entry Point — Navigation Flow Controller
