/// Global navigator key — allows OfflineQueue to show SnackBars from
/// outside the widget tree (e.g., from background connectivity callbacks).
final GlobalKey<NavigatorState> navigatorKey = GlobalKey<NavigatorState>();

void main() async {
  WidgetsFlutterBinding.ensureInitialized();

  // UXA-006 FIX: Limit ImageCache to prevent OOM on Infinite Scroll
  // Default is 1000 images or 100MB, which crashes low-end Syrian phones
  PaintingBinding.instance.imageCache.maximumSize = 100;
  PaintingBinding.instance.imageCache.maximumSizeBytes = 50 * 1024 * 1024; // 50 MB

  // Initialize Firebase with platform-specific options (generated by FlutterFire CLI)
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );

  // Initialize production API client
  await NammerhaApiClient.instance.init();

  // GAP-M1 PLATINUM: Initialize Crashlytics EARLY — before any other code runs.
  // Wires FlutterError.onError + PlatformDispatcher.onError to capture ALL
  // uncaught exceptions in production. Disabled in debug mode.
  CrashlyticsService.instance.init();

  // GAP-M2 PLATINUM: Initialize Performance Monitoring.
  // Captures app startup time, HTTP latency, and custom screen traces.
  PerformanceService.instance.init();

  // Initialize offline queue engine (loads persisted requests, starts connectivity monitor)
  await OfflineQueue.instance.init();

  // Wire OfflineQueue callbacks — notifies users when queued requests replay.
  // PLATINUM v2: These callbacks were defined but never wired. Users had zero
  // visibility into whether their offline mutations succeeded or silently failed.
  // P1-UXA-004 FIX: Bilingual OfflineQueue snackbar messages.
  // Previous: Hardcoded Arabic strings — unintelligible to English-locale users.
  // Now: Arabic primary + English fallback (matches error widget pattern).
  OfflineQueue.instance.onRequestReplayed = (request) {
    _showSyncSnackBar(
      message: 'تم إرسال طلب مؤجل بنجاح / Queued request sent: ${request.endpoint}',
      isError: false,
    );
  };

  OfflineQueue.instance.onRequestFailed = (request, error) {
    _showSyncSnackBar(
      message: 'فشل طلب مؤجل نهائياً / Queued request failed: ${request.endpoint}',
      isError: true,
    );
  };

  // P0-VE-001 PLATINUM: Initialize deep link service — intercepts
  // email verification and password reset links from the browser.
  // Must init AFTER Firebase but BEFORE runApp so cold-start links
  // are captured even before the navigator is mounted.
  await DeepLinkService.instance.init(navigatorKey);

  // Initialize push notification service (registers background handler, creates channel)
  await PushNotificationService.instance.init();

  // FRIC-2026-F09 FIX: Branded error boundary — replaces Flutter's red error screen.
  // CrashlyticsService captures the real error. This gives users a clean recovery UI
  // instead of a wall of red text. Uses brand colors + Phosphor icons.
  // P1-UXA-005 FIX: Uses platform locale to determine text direction.
  // Previous: Hardcoded TextDirection.rtl — English-locale users saw reversed layout.
  ErrorWidget.builder = (FlutterErrorDetails details) {
    // P1-UXA-005: Determine text direction from platform locale, not hardcoded.
    final platformLocale = WidgetsBinding.instance.platformDispatcher.locale;
    final isRtl = platformLocale.languageCode == 'ar' ||
        platformLocale.languageCode == 'he' ||
        platformLocale.languageCode == 'fa';
    final textDir = isRtl ? TextDirection.rtl : TextDirection.ltr;

    return MaterialApp(
      debugShowCheckedModeBanner: false,
      home: Directionality(
        textDirection: textDir,
        child: Scaffold(
          backgroundColor: const Color(0xFFF4F6F8), // --cloud-white
          body: Center(
            child: Padding(
              padding: const EdgeInsets.all(32),
              child: Column(
                mainAxisAlignment: MainAxisAlignment.center,
                children: [
                  Container(
                    width: 80,
                    height: 80,
                    decoration: BoxDecoration(
                      color: const Color(0xFFEF4444).withAlpha(15), // --error-red
                      borderRadius: BorderRadius.circular(20),
                    ),
                    child: Icon(
                      PhosphorIcons.warningOctagon(),
                      size: 40,
                      color: const Color(0xFFEF4444),
                    ),
                  ),
                  const SizedBox(height: 24),
                  // P1-UX-007: Bilingual error message — Arabic primary, English fallback
                  const Text(
                    'حدث خطأ غير متوقع\nAn unexpected error occurred',
                    style: TextStyle(
                      fontSize: 18,
                      fontWeight: FontWeight.w800,
                      color: Color(0xFF0F172A), // --text-heading
                      fontFamily: 'IBM Plex Sans Arabic',
                    ),
                    textAlign: TextAlign.center,
                  ),
                  const SizedBox(height: 8),
                  const Text(
                    'نعتذر عن هذا الخلل. يرجى إعادة تشغيل التطبيق.\nPlease restart the application.',
                    style: TextStyle(
                      fontSize: 14,
                      color: Color(0xFF64748B), // --text-secondary
                      fontFamily: 'IBM Plex Sans Arabic',
                    ),
                    textAlign: TextAlign.center,
                  ),
                ],
              ),
            ),
          ),
        ),
      ),
    );
  };

  runApp(const NammerhaMobileApp());
}

/// Shows a non-blocking SnackBar notification for offline queue sync events.
/// Uses the [navigatorKey] to access the current overlay without BuildContext.
void _showSyncSnackBar({required String message, required bool isError}) {
  final context = navigatorKey.currentContext;
  if (context == null) return;

  ScaffoldMessenger.of(context).showSnackBar(
    SnackBar(
      content: Text(
        message,
        style: const TextStyle(
          fontSize: 13,
          fontWeight: FontWeight.w500,
          color: Colors.white,
        ),
      ),
      backgroundColor: isError ? const Color(0xFFB00020) : const Color(0xFF0A6E55),
      behavior: SnackBarBehavior.floating,
      duration: const Duration(seconds: 4),
      margin: const EdgeInsets.all(12),
      shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(12)),
    ),
  );
}


